import numpy as np
arr1 = np.empty((4,2)) # 1. Empty Numpy Array
print("Empty Numpy Array: \n", arr1)

arr2 = np.full((4,2), 99) # 2. Numpy Full Array
print("Full Numpy Array: \n", arr2)

zero_arr = np.zeros((3,5)) # 3. Array Filled with all zeroes
print("Zeros Numpy Array: \n", zero_arr)

one_arr = np.ones((4,3,2)) # 4. Array filled with all ones
print("Ones Numpy Array: \n", one_arr)